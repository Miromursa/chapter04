
public class WhileLoop
{
    public WhileLoop()
    {

    }

    public void sampleMethod()
    {int theCurrentNumberWeArePrinting = 1;
        while(theCurrentNumberWeArePrinting <= 10) {
            System.out.println(theCurrentNumberWeArePrinting);
            theCurrentNumberWeArePrinting++;
        }
    } 

    public void multiples()
    {int theCurrentNumberWeArePrinting = 5;
        while(theCurrentNumberWeArePrinting <= 100) {
            System.out.println(theCurrentNumberWeArePrinting);
            theCurrentNumberWeArePrinting += 5;

        } }

}
